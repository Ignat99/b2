void SetCurrentThreadNamef(const char *fmt,...) {
    va_list v;

    va_start(v,fmt);
    SetCurrentThreadNamev(fmt,v);
    va_end(v);
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

void SetCurrentThreadNamev(const char *fmt,va_list v) {
    char *name;
    vasprintf(&name,fmt,v);

    SetCurrentThreadName(name);

    free(name);
    name=NULL;
}

